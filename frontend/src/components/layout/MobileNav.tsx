export default function MobileNav() {
  const pathname = usePathname();

  return (
    <nav className="ww-mobile-nav" aria-label="Mobile navigation">
      <ul className="ww-mobile-nav__items">
        {navItems.map((item) => {
          const isActive =
            item.href === '/'
              ? pathname === '/'
              : pathname.startsWith(item.href);

          return (
            <li key={item.href}>
              <Link
                href={item.href}
                className={`ww-mobile-nav__item ${
                  isActive ? 'ww-mobile-nav__item--active' : ''
                }`}
              >
                {item.icon}
                <span>{item.label}</span>
              </Link>
            </li>
          );
        })}
      </ul>
    </nav>
  );
}
